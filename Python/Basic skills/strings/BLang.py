my_str = input("Enter str:\n")
if "a" in my_str:
    my_str = my_str.replace("a","aba")
if "e" in my_str:
    my_str = my_str.replace("e","ebe")
if "i" in my_str:
    my_str = my_str.replace("i","ibi")
if "o" in my_str:
    my_str = my_str.replace("o","obo")
if "u" in my_str:
    my_str = my_str.replace("u","ubu")
print(my_str)